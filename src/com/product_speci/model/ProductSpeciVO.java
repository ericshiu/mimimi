package com.product_speci.model;


public class ProductSpeciVO {
	private String psp_no;
	private String pro_no;
	private String psp_name;
	private String psp_list;
	
	public String getPsp_no() {
		return psp_no;
	}
	public void setPsp_no(String psp_no) {
		this.psp_no = psp_no;
	}
	public String getPro_no() {
		return pro_no;
	}
	public void setPro_no(String pro_no) {
		this.pro_no = pro_no;
	}
	public String getPsp_name() {
		return psp_name;
	}
	public void setPsp_name(String psp_name) {
		this.psp_name = psp_name;
	}
	public String getPsp_list() {
		return psp_list;
	}
	public void setPsp_list(String psp_list) {
		this.psp_list = psp_list;
	}	
}
